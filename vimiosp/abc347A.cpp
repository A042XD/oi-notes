using namespace std;
const int N=105;
int n,k,a[N];
int read(){ // Fast Read
  int x=0,f=1;char c=getchar();
  while(c<'0'||c>'9'){if(c=='-')f=-1;c=getchar();}
  while(c>='0'&&c<='9'){x=x*10+c-48;c=getchar();}
  return x*f;
}

signed main(){
  n=read(),k=read();
  for(int i=1;i<=n;i++){
    a[i]=read();
    if(a[i]%k==0)printf("%d ",a[i]/k);
  }
  return 0;
}